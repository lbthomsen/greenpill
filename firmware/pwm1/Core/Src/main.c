void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);
	if (htim->Instance == TIM4) {

		// Calculate for all 3 leds
		for (uint8_t i = 0; i < 3; ++i) {

			// Calculate duty cycle.  Using cos rather than sin so that an angle of
			// 0 will switch the LED off.
			*(uint16_t*) led_ccr[i] = (uint16_t) (500 + arm_cos_f32(angle[i]) * 500);

			angle[i] += velocity[i]; // Next angle

			// We don't want to deal with massively large angles, so stay within
			// one full circle.
			if (angle[i] > M_PI2)
				angle[i] -= M_PI2;

		}

	}
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);
}

int _write(int file, char *ptr, int len) {
	/* Implement your write code here, this is used by puts and printf for example */
	for (int i = 0; i < len; i++)
		ITM_SendChar((*ptr++));
	return len;
}

void setFreq(uint8_t led, float freq) {
	velocity[led] = 2 * M_PI / (1000 / freq);
}

void setAngle(uint8_t led, float new_angle) {
	angle[led] = new_angle;
}

void resetAll() {
	for (uint8_t i = 0; i < 3; i++) {
		setFreq(i, 0);
		setAngle(i, 0);
	}
}

