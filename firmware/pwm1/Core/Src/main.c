  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

	// Store addresses for CCM registers
	led_ccr[0] = (void*) &TIM2->CCR1;
	led_ccr[1] = (void*) &TIM2->CCR2;
	led_ccr[2] = (void*) &TIM2->CCR3;

	// Start timer
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
